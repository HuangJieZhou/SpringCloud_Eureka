package org.hjz.invoked01.common.model;

import java.math.BigDecimal;

public class Area {
    private BigDecimal areaId;

    private String areaNo;

    private String areaName;

    private String areaType;

    private BigDecimal areaFid;

    private String areaCode;

    private String areaState;

    private String clzt;

    private BigDecimal areaSno;

    public BigDecimal getAreaId() {
        return areaId;
    }

    public void setAreaId(BigDecimal areaId) {
        this.areaId = areaId;
    }

    public String getAreaNo() {
        return areaNo;
    }

    public void setAreaNo(String areaNo) {
        this.areaNo = areaNo;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public String getAreaType() {
        return areaType;
    }

    public void setAreaType(String areaType) {
        this.areaType = areaType;
    }

    public BigDecimal getAreaFid() {
        return areaFid;
    }

    public void setAreaFid(BigDecimal areaFid) {
        this.areaFid = areaFid;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getAreaState() {
        return areaState;
    }

    public void setAreaState(String areaState) {
        this.areaState = areaState;
    }

    public String getClzt() {
        return clzt;
    }

    public void setClzt(String clzt) {
        this.clzt = clzt;
    }

    public BigDecimal getAreaSno() {
        return areaSno;
    }

    public void setAreaSno(BigDecimal areaSno) {
        this.areaSno = areaSno;
    }
}